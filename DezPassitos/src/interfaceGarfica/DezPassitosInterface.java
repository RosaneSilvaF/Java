    private void botaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoActionPerformed
        if(explodiu == false){
            String valor = ((JToggleButton)evt.getSource()).getText();

            if (passos<10) {
                boolean botaoEhBomba = jogo.confereBotao(valor);
                if(botaoEhBomba){
                    ((JToggleButton)evt.getSource()).setBackground(Color.red);
                    JOptionPane.showMessageDialog(this, "Explodiu.", "Fim de jogo.", JOptionPane.ERROR_MESSAGE);
                    explodiu = true;
                }
                passos++;
            }
            else{
                JOptionPane.showMessageDialog(this, "Vit√≥ria", "Fim de jogo.", JOptionPane.ERROR_MESSAGE);
                explodiu = true;
            }
                
        }
        if(explodiu == true){
            System.exit(0);
        }
        
    }//GEN-LAST:event_botaoActionPerformed

