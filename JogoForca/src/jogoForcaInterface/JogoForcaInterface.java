    private void organizaPainel() {                                               
        painel[0] = jTextField1;
        painel[1] = jTextField2;
        painel[2] = jTextField3;
        painel[3] = jTextField4;
        painel[4] = jTextField5;
        painel[5] = jTextField6;
        painel[6] = jTextField7;
        
        for (JTextField campo : painel) {
            campo.setFocusable(false);
        }
    }
    
    private void confereVitoria() { 
        String letraAux;
        boolean ganhou=false;
        if (!clicouConferir){
            
            for (int i = 0; i < 7; i++) {
                letraAux = "" + palavras[i];
                if (painel[i].getText().equals("     " + letraAux)) {
                    
                    ganhou=true;
                }
                else{
                    ganhou=false;
                    break;
                }
            }
            
            if(ganhou){
                JOptionPane.showMessageDialog(this, "Parabéns!! Você ganhou o jogo!", "Vitória", HEIGHT);
                System.exit(0);
            }
        }
    }
