    private void pressionaLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pressionaLetraActionPerformed
        String letraAux;
        boolean mensagem = true;
        ((JToggleButton)evt.getSource()).setEnabled(false);
        
        
        if (jogo.contagemVidas(false)){
            for (int i = 0; i < 7; i++) {

                if(((JToggleButton)evt.getSource()).getText().charAt(0) == palavras[i]){
                    letraAux="" + palavras[i];
                    letraAux = letraAux.toLowerCase();
                    painel[i].setText("     " + letraAux);   
                    mensagem = false;
                }

            }
            if(mensagem){
                
                jogo.contagemVidas(true);
                jTextField8.setText(jogo.mostraVidas());
                JOptionPane.showMessageDialog(this, "Letra incorreta. -1 chance", "Errou", HEIGHT);    
                
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Seus chutes acabaram. Você perdeu", "Fim de jogo", HEIGHT);
            System.exit(0);
        }
        confereVitoria();
    }//GEN-LAST:event_pressionaLetraActionPerformed

